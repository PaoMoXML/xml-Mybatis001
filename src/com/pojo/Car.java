package com.pojo;

public class Car {
    private Integer cId;

    private Integer cQuantity;

    private Integer cUserid;

    private Integer cGoodid;

    public Integer getcId() {
        return cId;
    }

    public void setcId(Integer cId) {
        this.cId = cId;
    }

    public Integer getcQuantity() {
        return cQuantity;
    }

    public void setcQuantity(Integer cQuantity) {
        this.cQuantity = cQuantity;
    }

    public Integer getcUserid() {
        return cUserid;
    }

    public void setcUserid(Integer cUserid) {
        this.cUserid = cUserid;
    }

    public Integer getcGoodid() {
        return cGoodid;
    }

    public void setcGoodid(Integer cGoodid) {
        this.cGoodid = cGoodid;
    }
}